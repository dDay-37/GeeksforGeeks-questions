class Solution:
    
    #Function to find the first position with different bits.
    def posOfRightMostDiffBit(self,m,n):
        b=1
        if m==n:
            return -1
        while m and n:
            if m&1 == n&1:
                b+=1
                m=m>>1
                n=n>>1
            else:
                break
        return b
        

